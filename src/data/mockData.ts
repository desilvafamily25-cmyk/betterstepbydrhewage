function generateCheckIns(patientId: string, startWeight: number, weeks: number): CheckIn[] {
  const checkIns: CheckIn[] = [];
  const today = new Date('2026-05-02');
  let weight = startWeight;

  for (let i = weeks; i >= 0; i--) {
    const date = new Date(today);
    date.setDate(date.getDate() - i * 7);
    const weeklyChange = (Math.random() - 0.35) * 0.8;
    weight = Math.max(weight - 2, weight + weeklyChange);

    const sideEffects: CheckIn['sideEffects'] = Math.random() > 0.7
      ? [(['nausea', 'constipation', 'reflux', 'dizziness'] as const)[Math.floor(Math.random() * 4)]]
      : ['none'];

    checkIns.push({
      id: `ci-${patientId}-${i}`,
      patientId,
      date: date.toISOString().split('T')[0],
      weightKg: Math.round(weight * 10) / 10,
      waistCm: Math.round((90 + Math.random() * 20 - i * 0.3) * 10) / 10,
      appetiteScore: Math.floor(Math.random() * 4) + 4,
      energyScore: Math.floor(Math.random() * 4) + 5,
      moodScore: Math.floor(Math.random() * 3) + 6,
      sleepScore: Math.floor(Math.random() * 4) + 5,
      exerciseLevel: (['none', 'light', 'moderate', 'light'] as const)[Math.floor(Math.random() * 4)],
      alcoholIntake: 'none',
      proteinFocus: Math.random() > 0.4,
      waterFocus: Math.random() > 0.3,
      sideEffects,
      notes: '',
      redFlag: false,
    });
  }
  return checkIns;
}

